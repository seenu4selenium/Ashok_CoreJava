package com.corejava;

public class ConditionalStatementsDemo {

	public static void main(String[] args) {
		int a = 20;
		int b = 90;

		if (a > b) {
			System.out.println("a is large value");
		} else if (b > a) {
			System.out.println("b is large value");
		} else {
			System.out.println("both are equal");
		}

	}

}
