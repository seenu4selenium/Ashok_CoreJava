
package com.corejava;


public class DataTypesDemo {

	
	public static void main(String[] args) {
	//	User will store numbers into a variable.
		byte b = 12;
		short c = 345;
		int a = 968979;
		long d = 1874632210;

		//User will store number with decimal values into a variable.
		float e = 12.123456789f;
		double f = 3245345.123456789;
		System.out.println(e);
		System.out.println(f);
		
		//User will store number/Alphabet/SpecialCharacter into a variable.
		//char data type you can declare into single quotes
		char abc ='4';
		//char abc ='p';
		//char abc ='&';
		
		//Want to store TRUE  or FALSE, will use boolean
		boolean h = true;
		boolean i = false;
		
	}

}
